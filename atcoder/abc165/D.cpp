void init() {
  ios_base::sync_with_stdio(0);
  cin.tie(0); cout.tie(0);
}

void solve() {
  long long a, b, n; scanf("%lld%lld%lld", &a, &b, &n);
  if (n >= b) printf("%lld", a*(b-1)/b);
  else printf("%lld", a*n/b);
}

int main() {
  init();
  int t = 1; //scanf("%d", &t);
  while (t--) {
    solve();
  }
  return 0;
}
