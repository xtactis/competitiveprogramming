void init() {
	ios_base::sync_with_stdio(0);
	cin.tie(0); cout.tie(0);
}

const int mod = 1e9+7;

void solve() {
	int n; scanf("%d", &n);
	long long ans = 1;
	for (int i = 1; i <= n; ++i) {
		(ans *= i) %= mod;
	}
	printf("%lld", ans);
}

int main() {
	init();
	int t = 1; //scanf("%d", &t);
	while (t--) {
		solve();
	}
	return 0;
}
