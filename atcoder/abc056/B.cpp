#endif

void init() {
	ios_base::sync_with_stdio(0);
	cin.tie(0); cout.tie(0);
}

void solve() {
	int w, a, b; cin >> w >> a >> b;
	if (a < b)
		cout << max(0, b-(a+w));
	else
		cout << max(0, a-(b+w));
}

int main() {
	init();
	int t = 1; //scanf("%d", &t);
	while (t--) {
		solve();
	}
	return 0;
}
